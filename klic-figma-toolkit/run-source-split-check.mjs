import fs from 'node:fs';
import path from 'node:path';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const root = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(root, '..');

function assert(condition, message) {
  if (!condition) throw new Error(message);
}

function run(command, args) {
  const result = spawnSync(command, args, {
    cwd: repoRoot,
    encoding: 'utf8',
    stdio: 'pipe',
  });
  if (result.stdout) process.stdout.write(result.stdout);
  if (result.stderr) process.stderr.write(result.stderr);
  assert(result.status === 0, `${command} ${args.join(' ')} failed with exit code ${result.status}`);
}

const requiredFiles = [
  'klic-figma-toolkit/build-toolkit.mjs',
  'klic-figma-toolkit/src/code/00-bootstrap.js',
  'klic-figma-toolkit/src/code/10-command-center.js',
  'klic-figma-toolkit/src/code/20-menu-generator.js',
  'klic-figma-toolkit/src/code/30-style-guide.js',
  'klic-figma-toolkit/src/code/40-table-builder.js',
  'klic-figma-toolkit/src/ui/index.html',
  'klic-figma-toolkit/src/ui/styles.css',
  'klic-figma-toolkit/src/ui/i18n.js',
  'klic-figma-toolkit/src/ui/app.js',
];

for (const relPath of requiredFiles) {
  assert(fs.existsSync(path.join(repoRoot, relPath)), `source split file is missing: ${relPath}`);
}

const i18n = fs.readFileSync(path.join(repoRoot, 'klic-figma-toolkit/src/ui/i18n.js'), 'utf8');
assert(i18n.includes('const I18N = {'), 'i18n source must own the I18N dictionary');
assert(i18n.includes('en:') && i18n.includes('ko:'), 'i18n source must include English and Korean dictionaries');

const generatedUi = fs.readFileSync(path.join(repoRoot, 'klic-figma-toolkit/ui.html'), 'utf8');
assert(generatedUi.includes('<!-- Generated by klic-figma-toolkit/build-toolkit.mjs. Do not edit directly. -->'), 'ui.html should be marked as generated');
assert(generatedUi.includes('const I18N = {'), 'generated ui.html should inline the i18n dictionary for Figma');

const generatedCode = fs.readFileSync(path.join(repoRoot, 'klic-figma-toolkit/code.js'), 'utf8');
assert(generatedCode.includes('// Generated by klic-figma-toolkit/build-toolkit.mjs. Do not edit directly.'), 'code.js should be marked as generated');
assert(generatedCode.includes("case 'command-refresh'"), 'generated code.js should include Command Center handlers');
assert(generatedCode.includes("case 'menu-register-template'"), 'generated code.js should include Menu Generator handlers');
assert(generatedCode.includes("case 'table-generate'"), 'generated code.js should include Table Builder handlers');

run('node', ['klic-figma-toolkit/build-toolkit.mjs', '--check']);

console.log('KLIC source split check passed.');
